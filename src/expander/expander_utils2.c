#include "../include/minishell.h"

char	**del_null_str(char **cmd, int *i)
{
	int	j;

	free(cmd[*i]);
	j = *i;
	while (cmd[j])
	{
		cmd[j] = cmd[j + 1];
		j++;
	}
	return (cmd);
}
